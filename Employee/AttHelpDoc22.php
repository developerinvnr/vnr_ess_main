	 <u>The different status shown are</u>:<br> 
     &nbsp;a)	<b>“Leave”</b>- for any approved leave types of SL,CL,EL,PL,FL or TL<br>
     &nbsp;b)	<b>“Holiday”</b>- for any public or declared holiday<br>
     &nbsp;c)	<b>“Outdoor Duty”</b>- for duty at locations other than office premises<br>
     &nbsp;d)	<b>“Present”</b>- status indicating presence on duty<br>
     &nbsp;e)	<b>“Absent”</b>- status indicating absent from duty<br>
     &nbsp;f)	<b>“SH”</b>- Half Sick Leave<br>
     &nbsp;g)	<b>“CH”</b>- Half Casual Leave<br><br>
	 
	 <b style="font-size:16px;">Important points to be noted…</b><br>
     &nbsp;•	The attendance at office locations shall be captured through the thumb reader machine. Employees have to swipe the thumb reader twice for recording complete attendance  i.e. once at the time of entry and the other when they leave office.<br>
     &nbsp;•	For office location staff, direct official visit at outlocation for whole day shall be considered as “outdoor duty” and will be marked accordingly. <br>
     &nbsp;•	The field staff and other office locations will have to send their attendance through SMS on 7489533422  or mail to HR by 10:30am everyday. <br>
     &nbsp;•	Those employees who have not completed the minimum hours conditions shall have half day CL deduction or absence mark for the every 3 days they have come late as per the office timings. In the event of there not being balance CL, the PL and then the EL shall be considered for deduction of leave for absence. If no leave is present then it will lead to absence status and shall lead to salary deduction.  <br>
     &nbsp;•	The compiled attendance will be posted in ESS every week for employee and reporting manager’s review.  <br> 
     &nbsp;•	Any discrepancies in attendance or regularisation of absent mark needs to brought to HR’s notice latest by 28th of every month and send the required approvals to HR for proper salary processing. <br>
     &nbsp;•	Simultaneously, for the office location staff employees, they shall also record attendance in the registers kept at the office premises for statutory compliance. <br>


