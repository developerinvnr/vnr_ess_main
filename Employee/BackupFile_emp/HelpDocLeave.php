	 <b style="font-size:16px;">The leave application field has following details to be filled;</b><br>
	 <b>Apply for leave</b>:<br>
     &nbsp;1.	Dates of leaves<br>
     &nbsp;2.	Contact no while on leave for emergency contact<br>
     &nbsp;3.	Address during leave for emergency<br>
     &nbsp;4.	Type of leaves ( refer the Leaves snapshot for leave combinations )<br>
     &nbsp;5.	Reason for leave for approval <br><br>

	 <b>My Leave</b> : Gives the opening balance, leave availed in the month and the closing balance for the running month.<br><br>
	 <b>Holiday List</b> : You can view the holiday list to plan your leaves. Consider the leave applicability as per your state.<br><br>
	 <b style="font-size:16px;">Important points to be noted…..</b><br>
	 <b>Employee  Level</b><br>
     &nbsp;•	Apply leave online before proceeding for  leave in case the leave is a planned leave<br>
	 &nbsp;•	For unplanned leave, apply after resuming duty.<br>
	 &nbsp;•	All leaves are to be applied and approval to be obtained from reporting manager before the salary processing in the ESS.<br>
	 &nbsp;•	The leaves have to be approved by the managers. <br>
	 &nbsp;•	If the leave is not approved by the reporting manager before the salary processing, the applied leave shall be treated as unauthorized/ unapproved and therefore marked as “Absent”.<br>
	 &nbsp;•	Please ensure that the leave rules are followed while applying for leave.<br>
	 &nbsp;•	Leave cancellation can be applied for, from the leave status given below the application box. The cancelled leave also has to be approved by the managers for effecting in the attendance. <br>

    <b>Reporting Manager Level</b><br>
	&nbsp;•	Approve the leave applications or leave cancellation applications for the team members before the salary processing  dates for proper salary processing.<br>
    &nbsp;•	In case, you want to deny leave, you may disapprove the same with proper reason for denial.<br>
    &nbsp;•	You can view the applications in the “team approvals” tab. <br>
    &nbsp;•	The various status of leave applications are.<br>
&nbsp;&nbsp;&nbsp;&nbsp;1.	Any leave applications pending for approval will appear in the “Draft/Pending” status<br>
&nbsp;&nbsp;&nbsp;&nbsp;2.	The approved leaves will appear in the Approved leaves<br>
&nbsp;&nbsp;&nbsp;&nbsp;3.	The disapproved leaves will appear in the disapproved leaves<br>
&nbsp;&nbsp;&nbsp;&nbsp;4.	The leaves applied for cancellations will appear in the “cancelled leaves”<br><br><br><br><br>
