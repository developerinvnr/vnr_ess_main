	 &nbsp;The <b>Performance Management system</b> is an Annual Appraisal of your performance. The appraisal cycle is from 1st October to 30th September  every year.<br><br>
     The <b>PMS</b> will be activated during the appraisal review process in the month of September. You can however enter / view your KRAs ( Key result areas) or goals as a guideline for measuring the performance. The behavioral parameters can also be viewed for effective display of expected behavior. <br><br>


     <b style="font-size:16px;"><i><u>Employee / Appraisee</u></i></b>
     &nbsp;•	Set your KRAs online from existing KRAs or add new KRAs after consultation with your reporting managers<br>
     &nbsp;•	The approved KRAs can be viewed or a print can be taken.<br>
     &nbsp;•	During the performance appraisals, your performance shall be measured on the KRAs and behavioral parameters through the activated link<br><br>
     <b style="font-size:16px;"><i><u>Reporting Manager/ Appraiser</u></i></b>
     &nbsp;•	Approve the KRAs online for the team<br>
     &nbsp;•	You have an option of sending back the KRAs for changes, if any<br>
     &nbsp;•	You can view the KRAs for your team at any point of the time<br>
     &nbsp;•	The appraisals will be visible once the PMS link is activated for assessment of  self & your team members<br><br>
     <b style="font-size:16px;"><i><u>Reviewer</u></i></b>
     &nbsp;•	You can view the KRAs/ behavioral parameters of all your team members<br>
     &nbsp;•	You can send back the KRAs for correction to appraiser.<br>
     &nbsp;•	The PMS link when activated, you can view and appraise yourself and the team members<br><br>
     <b style="font-size:16px;"><i><u>HOD</u></i></b>
     &nbsp;•	Can view the KRAs of all team members of the function<br>
     &nbsp;•	Can appraise the team members during the appraisal cycle.<br>
