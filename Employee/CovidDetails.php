   <center><b style="font-size:25px;color:#006BD7;"><u>Self-Declaration COVID prevention Guidelines</u></b></center><p>
   <b>
   Guidelines on preventive measures to contain spread of COVID-19 in workplace settings, Home Quarantine & Home isolation as issued by GOI Ministry of Health & family welfare. </b><p />
<b>The document is divided into the following subsections</b><br />
 <pre style="font-family:'Times New Roman;">
<b>i. </b> Basic preventive measures to be followed at all times
<b>ii. </b>Measures specific to offices
<b>iii.</b>Instructions for contacts being home quarantined
<b>iv. </b>Home Isolation 
</pre>

<b>1.Basic preventive measures</b><br />
<b>1.1.</b>The basic preventive measures include simple public health measures that are to be followed to reduce the risk of infection with COVID-19. These measures need to be observed by all (employees and visitors) at all times. These include:<br />
<b>i.</b>Physical distancing of at least one meter to be followed at all times.<br />
<b>ii.</b>Use of face covers/masks to be mandatory.<br />
<b>iii.</b>Practice frequent hand washing (for at least 40-60 seconds) even when hands are not visibly dirty and use of alcohol based hand sanitizers (for at least 20 seconds).<br />
<b>iv.</b>Respiratory etiquettes to be strictly followed. This involves strict practice of covering ones mouth and nose while coughing/sneezing with a tissue/handkerchief/flexed elbow and disposing off used tissues properly.<br />
<b>v.</b>Self-monitoring of health by all and reporting any illness at the earliest.<p />

<b>2.Preventive measures for offices:</b><br />
<b>2.1</b>Any staff reportedly suffering from flu-like illness should not attend office and seek medical advice from local health authorities Such persons, if diagnosed as a suspect/confirmed case of COVID-19 should immediately inform the office authorities.<br />
<b>2.2.</b>Discourage, to the maximum extent, entry of visitors. Routine issue of visitors/temporary passes should be suspended with immediate effect. Only those visitors whom have proper permission who they want to meet, should be allowed after being properly screened.<br />
<b>i.</b>Meetings, as far as feasible, should be done through video conferencing. To minimize or reschedule meetings involving large number of people unless necessary.<br />
<b>ii.</b>Avoid non-essential official travel.<br />
<b>iii.</b>Undertake essential correspondence on official email and avoid sending files and documents to other offices, to the extent possible.<br />
<b>iv.</b>Facilitate delivery and receipt of courier at the entry point itself, as far as practicable.<br />
<b>v.</b>Ensure proper cleaning and frequent sanitization of the workplace, particularly of the frequently touched surfaces.<br />
<b>vi.</b>Ensure regular supply of hand sanitizers, soap and running water in the washrooms.<br />
<b>vii.</b>All officials may be advised to take care of their own health and look out for respiratory symptoms/fever and, if feeling unwell, should leave the workplace immediately after informing their reporting Managers. They should observe home-quarantine as per the guidelines <br />
<b>viii.</b>The leave sanctioning authorities are advised to sanction leave whenever any request is made for self-quarantine as a precautionary measure.<br />
<b>ix.</b>Advise all employees who are at higher risk i.e. older employees, pregnant employees and employees who have underlying medical conditions, to take extra precautions. The Departments may take care not to expose such employees to any front-line work requiring direct contact with the public.<p />

<b>3.Instructions for contacts being home quarantined</b><br />
<b>3.1</b> The home quarantined person should stay in a well-ventilated single-room preferably with an attached/separate toilet. If another family member needs to stay in the same room, it's advisable to maintain a distance of at least 1 meter between the two.<br />
<b>3.2</b>Needs to stay away from elderly people, pregnant women, children and persons with co-morbidities within the household. 
<b>3.3</b>Restrict his/her movement within the house.<br />
<b>3.4</b>Under no circumstances attend any social/religious gathering e.g. wedding, condolences, etc.<p />
<b>3.5</b>He should also follow the under mentioned public health measures at all times:<br />
<b>a.</b>Wash hand as often thoroughly with soap and water or with alcohol-based hand sanitizer<br />
<b>b.</b>Avoid sharing household items e.g. dishes, drinking glasses, cups, eating utensils, towels, bedding, or other items with other people at home.<br />
<b>c.</b>Wear a surgical mask at all the time. The mask should be changed every 6-8 hours and disposed off Disposable masks are never to be reused.<br />
<b>d.</b>Masks used by patients / care givers/ close contacts during home care should be disinfected using ordinary bleach solution (5%) or sodium hypochlorite solution (1%) and then disposed of either by burning or deep burial.<br />
<b>e.</b>Used mask should be considered as potentially infected.<br />
<b>f.</b>If symptoms appear (cough/fever/difficulty in breathing), he/she should immediately inform the nearest health center or call 011-23978046.<p />

<b>4.Instructions for the family members of persons being home quarantined</b><br />
<b>4.1</b>Only an assigned family member should be tasked with taking care of the such person<br />
<b>4.2</b>Avoid shaking the soiled linen or direct contact with skin<br />
<b>4.3</b>Use disposable gloves when cleaning the surfaces or handling soiled linen<br />
<b>4.4</b>Wash hands after removing gloves<br />
<b>4.5</b>Visitors should not be allowed<br />
<b>4.6</b>In case the person being quarantined becomes symptomatic, all his close contacts will be home quarantined (for 14 days) and followed up for an additional 14days or till the report of such case turns out negative on lab testing<p />

<b>5.Environmental sanitation</b><br />
<b>5.1</b> Clean and disinfect frequently touched surfaces in the quarantined person's room (e.g. bed frames, tables etc.) daily with 1%Sodium Hypochlorite Solution.<br />
<b>5.2</b>Clean and disinfect toilet surfaces daily with regular household bleach solution/phenolic disinfectants<br />
<b>5.3</b>Clean the clothes and other linen used by the person separately using common household detergent and dry.<p /> 



<b>6.Duration of home quarantine</b><br />
<b>6.1</b>The home quarantine period is for 14 days from contact with a confirmed case or earlier if a suspect case (of whom the index person is a contact) turns out negative on laboratory testing.<p />

<b>7.Home Isolation </b><br />
<b>7.1</b>Eligibility for home isolation-<br />
<b>i.</b>The person should be clinically assigned as a very mild case/ pre-symptomatic case by the treating medical officer.<br />
<b>ii.</b>Such cases should have the requisite facility at their residence for self-isolation and also for quarantining the family contacts.<br />
<b>iii.</b>A care giver should be available to provide care on 24 x7 basis. A communication link between the caregiver and hospital is a prerequisite for the entire duration of home isolation.<br />
<b>iv.</b>The care giver and all close contacts of such cases should take Hydroxychloroquine prophylaxis as per protocol and as prescribed by the treating medical officer.<br />
<b>v.</b>Download Arogya Setu App on mobile (available at: https://www.mygov.in/aarogya-setuapp/) and it should remain active at all times (through Bluetooth and Wi-Fi)<br />
<b>vi.</b>The patient shall agree to monitor his health and regularly inform his health status to the District Surveillance Officer for further follow up by the surveillance teams.<br />
<b>vii.</b>The patient will fill in an undertaking on self-isolation and shall follow home quarantine guidelines. Such individual shall be eligible for home isolation.<p />

<b>7.2</b>Instructions for care-givers<br />
<b>i.</b>Mask: The caregiver should wear a triple layer medical mask appropriately when in the same room with the ill person. <br />
<b>ii.</b>He/she should avoid touching own face, nose or mouth.<br />
<b>iii.</b>Hand hygiene must be ensured following contact with ill person or his immediate environment.<br />
<b>iv.</b>Hand hygiene should also be practiced before and after preparing food, before eating, after using the toilet, and whenever hands look dirty. Use soap and water for hand washing at least for 40 seconds. Alcohol-based hand rub can be used, if hands are not visibly soiled.<br />
<b>v.</b>Exposure to patient: Avoid direct contact with body fluids of the patient, particularly oral or respiratory secretions. Use disposable gloves while handling the patient. Perform hand hygiene before and after removing gloves.<br />
<b>vi.</b>Avoid exposure to potentially contaminated items in his immediate environment (e.g. avoid sharing cigarettes, eating utensils, dishes, drinks, used towels or bed linen).<br />
<b>vii.</b>Food must be provided to the patient in his room<br />
<b>viii.</b>Utensils and dishes used by the patient should be cleaned with soap/detergent and water wearing gloves. The utensils and dishes may be re-used. Clean hands after taking off gloves or handling used items.<br />
<b>ix.</b>Use triple layer medical mask and disposable gloves while cleaning or handling surfaces, clothing or linen used by the patient. Perform hand hygiene before and after removing gloves.<br />
<b>x.</b>The care giver will make sure that the patient follows the prescribed treatment.<br />
<b>xi.</b>The care giver and all close contact will self-monitor their health with daily temperature monitoring and report promptly if they develop any symptom suggestive of COVID-19 (fever/cough/difficulty in breathing)<p />

<b>7.3</b> Instructions for the patient<br />
<b>i.</b>Patient should at all times use triple layer medical mask. Discard mask after 8 hours of use or earlier if they become wet or visibly soiled.<br />
<b>ii.</b>Mask should be discarded only after disinfecting it with 1% Sodium Hypo-chlorite.<br />
<b>iii.</b>Patient must stay in the identified room and away from other people in home, especially elderlies and those with co-morbid conditions like hypertension, cardiovascular disease, renal disease etc.<br />
<b>iv.</b>Patient must take rest and drink lot of fluids to maintain adequate hydration<br />
<b>v.</b>Follow respiratory etiquettes all the time.<br />
<b>vi.</b>Hands must be washed often with soap and water for at least 40 seconds or clean with alcohol-based sanitizer.<br />
<b>vii.</b>Don't share personal items with other people.<br />
<b>viii.</b>Clean surfaces in the room that are touched often (tabletops, door knobs, handles, etc.) with 1% hypochlorite solution.<br />
<b>ix.</b>The patient must strictly follow the physician's instructions and medication advice.<br />
<b>x.</b>The patient will self-monitor his/her health with daily temperature monitoring and report promptly if develops any deterioration of symptom as detailed below.<p />


<b>8 When to seek medical attention?</b><br />
<b>8.1</b> Patient / Care giver will keep monitoring their health. Immediate medical attention must be sought if serious signs or symptoms develop. These could include<br />
<b>i.</b>Difficulty in breathing<br />
<b>ii.</b>Persistent pain/pressure in the chest,<br />
<b>iii.</b>Mental confusion or inability to arouse,<br />
<b>iv.</b>Developing bluish discolorations of lips/face and<br />
<b>v.</b>As advised by treating medical officer<p />

<b>9 When to discontinue home isolation?</b><br />
Patient under home isolation will end home isolation after 17 days of onset of symptoms (or date of sampling, for pre-symptomatic cases) and no fever for 10 days. There is no need for testing after the home isolation period is over.
   
 
